int		ft_sqrt(int nb)
{
	long	sqrt;

	sqrt = 0;
	while (sqrt * sqrt < nb)
	{
		sqrt++;
	}
	if (sqrt * sqrt != nb)
		return (0);
	else
		return (sqrt);
}
